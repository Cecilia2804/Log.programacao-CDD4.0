#ler um valor e escrever a mensagem É MAIOR QUE 10! se o valor lido for maior que 10,
#caso contrario escrever NÃO É MAIOR QUE 10!
valor = int(input("escolha um valor para saber se ele é maior ou menor que 10:"))
if valor > 10:
    print(valor, "É MAIOR QUE 10")
else:
    print(valor, "NÃO É MAIOR QUE 10")
